package day21_WhileDoWhile;

public class WhileLoop {
	public static void main(String[] args){

		if (true) {
			System.out.println("Hello batch 23");
		}

		System.out.println("---------------------------------------");
		//while loop is a repeated if statement

		while (true){
			System.out.println("Hello batch 23");
		}
	}
}